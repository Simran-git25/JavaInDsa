public class Loops {
    public static void main(String [] args){
        int n=5;
        for(int i=0;i<=n;i++){
            System.out.println(i);
        }
        for(int i=n;i>=1;i--){
            System.out.println(i);
        }
        for(int i=1;i<=10;i++){
            System.out.println(n + "x" + i + "=" + n*i);
        }
        for(int i=1;i<=100;i++){
            System.out.print("Simran ");
        }
    }
}
